package pojo;

public class FavoriteGenres {
    
    private Integer favoriteGenresId;
    private Integer userId;
    private Integer genresId;
    
    public Integer getFavoriteGenresId() {
        return favoriteGenresId;
    }
    public void setFavoriteGenresId(Integer favoriteGenresId) {
        this.favoriteGenresId = favoriteGenresId;
    }
    public Integer getUserId() {
        return userId;
    }
    public void setUserId(Integer userId) {
        this.userId = userId;
    }
    public Integer getGenresId() {
        return genresId;
    }
    public void setGenresId(Integer genresId) {
        this.genresId = genresId;
    }

}
